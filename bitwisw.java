//Bitwise opperation
import java.util.*;
class bitwise
{
    public static void main(String args[])
    {
        int a=23,b=18;
        System.out.println("Bitwise opperation of AND: " +(a&b));
        System.out.println("Bitwise opperation of OR:" + (a|b));
        System.out.println("Bitwise opperation of X-OR: " +(a^b));
        System.out.println("Bitwise opperation of NOT of A: " +(~a));
        System.out.println("Bitwise opperation of NOT of B: " +(~b));
    }
}